async function migrate() {
  try {
    const pool = await sql.connect(dbConfig);
    console.log('Connected to SQL Server');

    const keys = Object.keys(translations.en);
    for (const key of keys) {
      console.log(`Migrating key: ${key}`);
      await pool.request()
        .input('key', sql.VarChar, key)
        .input('en', sql.NVarChar, translations.en[key])
        .input('ar', sql.NVarChar, translations.ar[key])
        .query(`
          IF NOT EXISTS (SELECT 1 FROM WEB_TRANSLATIONS WHERE TRANSLATION_KEY = @key)
            INSERT INTO WEB_TRANSLATIONS (TRANSLATION_KEY, EN_VALUE, AR_VALUE) 
            VALUES (@key, @en, @ar)
          ELSE
            UPDATE WEB_TRANSLATIONS SET EN_VALUE = @en, AR_VALUE = @ar WHERE TRANSLATION_KEY = @key
        `);
    }

    console.log('Migration completed successfully!');
    await pool.close();
  } catch (err) {
    console.error('Migration failed:', err);
  }
}

migrate();
